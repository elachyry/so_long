#include "../../includes/so_long.h"

int	key_release(int keycode, t_ghost *app)
{
	if (keycode == 124 || keycode == 123
		|| keycode == 126 || keycode == 125
		|| keycode == 49)
		app->is_long_press = 0;
	return (0);
}
